	BGMACDUMPREG(sc,BGMAC_REG_DEVICE_CONTROL);			\
	BGMACDUMPREG(sc,BGMAC_REG_DEVICE_STATUS);			\
	BGMACDUMPREG(sc,BGMAC_REG_BIST_STATUS);				\
	BGMACDUMPREG(sc,BGMAC_REG_INTR_STATUS);				\
	BGMACDUMPREG(sc,BGMAC_REG_INTERRUPT_MASK);			\
